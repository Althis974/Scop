#include "libft.h"

char	*ft_strdup(const char *src)
{
	int			i;
	int			len;
	char		*dest;

	len = 0;
	while (src[len] != '\0')
		len++;
	if ((dest = (char*)malloc(sizeof(char) * (len + 1))) == NULL)
		return (NULL);
	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
