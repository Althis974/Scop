#include "libft.h"

void	set_matid(t_mat *m, int order)
{
	int i;
	int j;

	i = -1;
	while (++i < order)
	{
		j = -1;
		while (++j < order)
			m->m[i][j] = j % order == 0 ? 1 : 0;
	}
}