#include "libft.h"

void	ft_swap(void *const a, void *const b, size_t size)
{
	void *tmp;

	if (!(tmp = malloc(size)))
		return ;
	ft_memcpy(tmp, a, size);
	ft_memcpy(a, b, size);
	ft_memcpy(b, tmp, size);
	free(tmp);
}
